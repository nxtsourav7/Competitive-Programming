#include<bits/stdc++.h>
using namespace std;

void S0LVE() {
    int n;
    cin >> n;

    for(int i = 1; i <= n; ++i) {
        cout << i << ' ';
    }
}

int32_t main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int Q;
    cin >> Q;
    while(Q--) {
        S0LVE();
        cout << '\n';
    }

    return 0;
}